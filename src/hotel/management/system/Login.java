    private void LOGINMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LOGINMousePressed
        // TODO add your handling code here:
        
        
        String user,pass;
        user = username.getText();
        pass = new String(password.getText());
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");  //Loads the database class
            Connection con;                             //Connection object created
            con=DriverManager.getConnection("JDBC:mysql://localhost:3306/mysql","root","");     //getting a connection to the database
            Statement stmt;                             //Statement variable to access to our database
            stmt=con.createStatement();                 //
            stmt.executeUpdate("USE hotelmanagementsystem");            //kon database use korbo ta select korlam
            ResultSet rs = stmt.executeQuery("select * from login;");   //Login table er sob column niye execute korbe SQL query
            int flag=0;
            while(rs.next()){                                   //rs holo ekta pointer jeta table er row gulo ke point kore...next() er maddhome porer row te point kore.
                String usr = rs.getString("username");          //jotokhon porjonto row ase while cholbe.
                String pas = rs.getString("password");
                if(user.equals(usr) && pass.equals(pas)){       //if Username & Password match hoy
                    flag=1;
                    break;
                }
            }
            rs.close();
            if(flag==1){
                new MainScreen().setVisible(true);
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(this, "Invalid user id or Password");
                username.setText("");
                password.setText("");
            }
        }
        catch(Exception e)
        {
            System.out.println("Exception: "+e);
        }
    }//GEN-LAST:event_LOGINMousePressed

    
    
    
